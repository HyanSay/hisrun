package com.hisrun.vo;

public class ResPolicyNo {
	// 当前保单号
	private String currentPolicyNo;
	// 开始保单号
	private String startPolicyNo;
	// 结束保单号
	private String endPolicyNo;

	public String getCurrentPolicyNo() {
		return currentPolicyNo;
	}

	public void setCurrentPolicyNo(String currentPolicyNo) {
		this.currentPolicyNo = currentPolicyNo;
	}

	public String getStartPolicyNo() {
		return startPolicyNo;
	}

	public void setStartPolicyNo(String startPolicyNo) {
		this.startPolicyNo = startPolicyNo;
	}

	public String getEndPolicyNo() {
		return endPolicyNo;
	}

	public void setEndPolicyNo(String endPolicyNo) {
		this.endPolicyNo = endPolicyNo;
	}

}
